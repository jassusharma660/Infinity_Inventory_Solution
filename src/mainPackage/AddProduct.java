    private void uploadButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uploadButtonMouseClicked
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        
        try{
            File f = chooser.getSelectedFile();
            filename = f.getAbsolutePath();

            /*
                Re-sizing acc. to ratio
            */
            double imgHeight = new ImageIcon(filename).getImage().getHeight(this),
            imgWidth = new ImageIcon(filename).getImage().getWidth(this),
            labelHeight = productImage.getHeight(),
            labelWidth = productImage.getWidth();
            int newHeight = 1, newWidth = 1;
            if(imgHeight < labelHeight) {
                newHeight = (int) (imgHeight * (labelWidth / imgWidth))/2;
                newWidth = (int) labelWidth/2;
            }
            else {
                newWidth = (int) (imgWidth * (labelHeight / imgHeight))/2;
                newHeight = (int) labelHeight/2;
            }
            //System.out.println("Image: "+imgWidth+" "+imgHeight+"Label: "+labelWidth+" "+labelHeight+"New: "+newWidth+" "+newHeight);
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(newWidth, newHeight, Image.SCALE_SMOOTH));
            productImage.setIcon(imageIcon);
        }
        catch(Exception e) {
            
        }
    }//GEN-LAST:event_uploadButtonMouseClicked

