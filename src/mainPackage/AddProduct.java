    public AddProduct(String id) {
        initComponents();
        isUpdate = true;
        this.setTitle("Update");
        textLabel.setText("Update Product");
        saveButtonPanel.removeAll();
        resetButtonPanel.removeAll();
        imagePanel.removeAll();
        uploadButtonPanel.removeAll();
        idForUpdate = id;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql:///newDB", "root", "");
            PreparedStatement s = con.prepareStatement("SELECT * FROM productDBForJavaApp WHERE pId = ?");
            s.setString(1, id);
            
            PreparedStatement s1 = con.prepareStatement("SELECT * FROM categoryDBForJavaApp WHERE categoryId = ?");
                
            ResultSet rs = s.executeQuery();
            if(rs.next()) {
                productName.setText(rs.getString("pName"));
                productUnit.setText(rs.getString("unit"));
                productQuantity.setText(rs.getString("quantity"));
                actualPrice.setText(rs.getString("actualPrice"));
                sellingPrice.setText(rs.getString("sellingPrice"));
                discount.setText(rs.getString("discount"));
                gstTax.setText(rs.getString("gst"));
                productDescription.setText(rs.getString("pDesc"));
                
                s1.setString(1, rs.getString("categoryId"));
                ResultSet rs1 = s1.executeQuery();
                if(rs1.next()) {
                    selectedCategoryCode = rs1.getString("categoryId");
                    productCategory.setText(rs1.getString("categoryDesc"));
                }
                
            }
            else {
                JOptionPane.showMessageDialog(null,"This item is not available anymore in the Database!");
                this.dispose();
                this.setVisible(false);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Some error occured!"+e);
        }
    }
    
